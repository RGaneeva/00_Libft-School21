#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	int		i;
	char	end;

	i = 0;
	end = '\n';
	if (s)
	{
		while (s[i] != '\0')
		{
			write (fd, &s[i], 1);
			i++;
		}
		write (fd, &end, 1);
	}
}
